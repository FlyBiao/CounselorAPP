package com.cesaas.android.number.calendar.entity;

/**
 * 日期Bean
 * @author fgb
 *
 */
public class DateInfoBean {

	public int year;
	public int month;
	public int day;
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	
}
